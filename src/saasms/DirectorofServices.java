    private void downloadRosterAsCSV() {
    JFileChooser fileChooser = new JFileChooser();
    fileChooser.setDialogTitle("Save Roster As");
    fileChooser.setSelectedFile(new java.io.File("roster.csv")); // default filename

    int userSelection = fileChooser.showSaveDialog(this); // `this` = your JFrame

    if (userSelection == JFileChooser.APPROVE_OPTION) {
        java.io.File fileToSave = fileChooser.getSelectedFile();

        // Ensure .csv extension
        String filePath = fileToSave.getAbsolutePath();
        if (!filePath.toLowerCase().endsWith(".csv")) {
            fileToSave = new java.io.File(filePath + ".csv");
        }

        try (FileWriter fw = new FileWriter(fileToSave)) {
            DefaultTableModel model = (DefaultTableModel) jTableRosters.getModel();

            // Write headers
            for (int i = 0; i < model.getColumnCount(); i++) {
                fw.write(model.getColumnName(i));
                if (i < model.getColumnCount() - 1) fw.write(",");
            }
            fw.write("\n");

            // Write data rows
            for (int i = 0; i < model.getRowCount(); i++) {
                for (int j = 0; j < model.getColumnCount(); j++) {
                    fw.write(model.getValueAt(i, j).toString());
                    if (j < model.getColumnCount() - 1) fw.write(",");
                }
                fw.write("\n");
            }

            JOptionPane.showMessageDialog(this, "Roster CSV saved to: " + fileToSave.getAbsolutePath());
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error saving CSV: " + e.getMessage());
        }
    }
}

