    private void loadDashboardStats() {
        try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/staffattendanceandshiftmanagementschema", "root", "Adesokan2310*")) {

            // Total Staff
            PreparedStatement ps1 = con.prepareStatement("SELECT COUNT(*) FROM users_tb");
            ResultSet rs1 = ps1.executeQuery();
            if (rs1.next()) {
                jLabelTotalStaff.setText(rs1.getString(1));
            }

            // Total Units
            PreparedStatement ps2 = con.prepareStatement("SELECT COUNT(DISTINCT unit) FROM users_tb");
            ResultSet rs2 = ps2.executeQuery();
            if (rs2.next()) {
                jLabelTotalUnits.setText(rs2.getString(1));
            }

            // Average Attendance Rate
            PreparedStatement ps3 = con.prepareStatement(
                    "SELECT ROUND((COUNT(*) / (SELECT COUNT(*) FROM attendance_logs)) * 100, 1) AS attendance_rate FROM attendance_logs WHERE status = 'present'"
            );
            ResultSet rs3 = ps3.executeQuery();
            if (rs3.next()) {
                jLabelAttendanceRate.setText(rs3.getString("attendance_rate") + "%");
            }

            // Shift Swaps This Week
            PreparedStatement ps4 = con.prepareStatement(
                    "SELECT COUNT(*) FROM shift_swap_requests WHERE WEEK(request_time) = WEEK(CURDATE())"
            );
            ResultSet rs4 = ps4.executeQuery();
            if (rs4.next()) {
                jLabelShiftSwaps.setText(rs4.getString(1));
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

