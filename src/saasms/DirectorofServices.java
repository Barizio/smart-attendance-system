   private void applyRosterFilter() {
    DefaultTableModel model = (DefaultTableModel) jTableRosters.getModel();
    model.setRowCount(0); // Clear existing rows

    String baseQuery = "SELECT sr.shift_date, u.staff_id, u.name, u.unit, s.shift_type, s.start_time, s.end_time "
                     + "FROM staff_rosters sr "
                     + "JOIN users_tb u ON sr.staff_id = u.staff_id "
                     + "JOIN shifts s ON sr.shift_id = s.shift_id WHERE 1=1";

    List<Object> parameters = new ArrayList<>();

    // Build query dynamically
    if (!filterText.getText().trim().isEmpty()) {
        baseQuery += " AND u.staff_id = ?";
        parameters.add(Integer.parseInt(filterText.getText().trim()));
    }

    if (filterDate.getDate() != null) {
        baseQuery += " AND sr.shift_date = ?";
        parameters.add(new java.sql.Date(filterDate.getDate().getTime()));
    }

    if (rosterUnitComboBox.getSelectedItem() != null && !"All Units".equals(unitComboBox.getSelectedItem())) {
        baseQuery += " AND u.unit = ?";
        parameters.add(rosterUnitComboBox.getSelectedItem().toString());
    }

    // Connect to DB and apply filters
    try (Connection con = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/staffattendanceandshiftmanagementschema",
            "root", "Adesokan2310*")) {

        PreparedStatement ps = con.prepareStatement(baseQuery);

        // Bind parameters
        for (int i = 0; i < parameters.size(); i++) {
            ps.setObject(i + 1, parameters.get(i));
        }

        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getDate("shift_date"),
                rs.getInt("staff_id"),
                rs.getString("name"),
                rs.getString("unit"),
                rs.getString("shift_type"),
                rs.getTime("start_time"),
                rs.getTime("end_time")
            });
        }

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error applying filters: " + e.getMessage());
    }
}


