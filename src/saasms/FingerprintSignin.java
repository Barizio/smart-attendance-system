    private void setupCaptureEvents() {
    capturer.addDataListener(new DPFPDataAdapter() {
        @Override
        public void dataAcquired(DPFPDataEvent e) {
            DPFPSample sample = e.getSample();
            processSample(sample);
        }
    });

    capturer.addReaderStatusListener(new DPFPReaderStatusAdapter() {
        public void readerConnected(DPFPReaderStatusEvent e) {
            statusLabel.setText("Scanner connected.");
        }

        public void readerDisconnected(DPFPReaderStatusEvent e) {
            statusLabel.setText("Scanner disconnected.");
        }
    });
}
    private void restartScanner() {
    try {
        capturer.stopCapture();  // Stop current scanning session
        capturer.startCapture(); // Start a new scanning session
        statusLabel.setText("Scanner ready. Place your finger.");
    } catch (Exception e) {
        statusLabel.setText("Error restarting scanner.");
        e.printStackTrace();
    }
}

private void processSample(DPFPSample sample) {
    displayFingerprintImage(sample);
    
    if (System.currentTimeMillis() - lastScanTime < COOLDOWN_SECONDS * 1000) {
        statusLabel.setText("Please wait before scanning again...");
        restartScanner();
        return;
    }
    lastScanTime = System.currentTimeMillis();

    try {
        features = DPFPGlobal.getFeatureExtractionFactory()
            .createFeatureExtraction().createFeatureSet(sample, DPFPDataPurpose.DATA_PURPOSE_VERIFICATION);
    } catch (DPFPImageQualityException ex) {
        statusLabel.setText("Poor fingerprint quality.");
        restartScanner();
        return;
    }

    if (features == null) {
        statusLabel.setText("Could not extract fingerprint features.");
        restartScanner();
        return;
    }

    matchAgainstDatabase();
}

private void matchAgainstDatabase() {
    try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/staffattendanceandshiftmanagementschema", "root", "Adesokan2310*")) {

        PreparedStatement ps = con.prepareStatement("SELECT staff_id, name, unit, email, fingerprint_template FROM users_tb");
        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            byte[] data = rs.getBytes("fingerprint_template");
            if (data == null) continue;

            referenceTemplate = DPFPGlobal.getTemplateFactory().createTemplate();
            referenceTemplate.deserialize(data);

            if (verifier.verify(features, referenceTemplate).isVerified()) {
                int staffId = rs.getInt("staff_id");
                String name = rs.getString("name");
                String unit = rs.getString("unit");
                String email = rs.getString("email");

                nameLabel.setText(name);

                // Pass email to logAttendance
                logAttendance(staffId, name, unit);
                return;
            }
        }

        statusLabel.setText("Fingerprint not recognized.");
        nameLabel.setText("");

    } catch (Exception e) {
        statusLabel.setText("Error: " + e.getMessage());
        e.printStackTrace();
    }
}
