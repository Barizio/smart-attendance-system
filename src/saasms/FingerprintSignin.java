private void logAttendance(int staffId, String name, String unit) {
    try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/staffattendanceandshiftmanagementschema", "root", "Adesokan2310*")) {

        LocalDate today = LocalDate.now();
        LocalTime now = LocalTime.now();
        String shiftType = null;
        String status;

        if (!unit.equalsIgnoreCase("Horticulture")) {
            PreparedStatement ps = con.prepareStatement("""
                SELECT s.shift_type, s.start_time, s.end_time 
                FROM staff_rosters sr 
                JOIN shifts s ON sr.shift_id = s.shift_id 
                WHERE sr.staff_id = ? AND sr.shift_date = ?
            """);
            ps.setInt(1, staffId);
            ps.setDate(2, Date.valueOf(today));
            ResultSet rs = ps.executeQuery();

            if (!rs.next()) {
                statusLabel.setText("No shift assigned today.");
                return;
            }

            shiftType = rs.getString("shift_type");
            LocalTime start = rs.getTime("start_time").toLocalTime();
            LocalTime end = rs.getTime("end_time").toLocalTime();
            boolean isOvernight = end.isBefore(start);

            PreparedStatement check = con.prepareStatement("""
                SELECT * FROM attendance_logs 
                WHERE staff_id = ? AND log_date = ? AND shift_type = ? AND sign_out_time IS NULL
            """);
            check.setInt(1, staffId);
            check.setDate(2, Date.valueOf(today));
            check.setString(3, shiftType);
            ResultSet log = check.executeQuery();

            if (log.next()) {
                status = !isOvernight && now.isBefore(end) ? "Early Sign-out" : "Signed Out";

                PreparedStatement out = con.prepareStatement("""
                    UPDATE attendance_logs SET sign_out_time = ?, status = ? WHERE log_id = ?
                """);
                out.setTime(1, Time.valueOf(now));
                out.setString(2, status);
                out.setInt(3, log.getInt("log_id"));
                out.executeUpdate();
            } else {
                status = !isOvernight && now.isAfter(start.plusMinutes(15)) ? "Late Sign-in" : "Signed In";

                PreparedStatement in = con.prepareStatement("""
                    INSERT INTO attendance_logs (staff_id, log_date, sign_in_time, status, unit, shift_type)
                    VALUES (?, ?, ?, ?, ?, ?)
                """);
                in.setInt(1, staffId);
                in.setDate(2, Date.valueOf(today));
                in.setTime(3, Time.valueOf(now));
                in.setString(4, status);
                in.setString(5, unit);
                in.setString(6, shiftType);
                in.executeUpdate();
            }
        } else {
            PreparedStatement check = con.prepareStatement("""
                SELECT * FROM attendance_logs WHERE staff_id = ? AND log_date = ? AND shift_type IS NULL AND sign_out_time IS NULL
            """);
            check.setInt(1, staffId);
            check.setDate(2, Date.valueOf(today));
            ResultSet log = check.executeQuery();

            if (log.next()) {
                status = "Signed Out";
                PreparedStatement out = con.prepareStatement("""
                    UPDATE attendance_logs SET sign_out_time = ?, status = ? WHERE log_id = ?
                """);
                out.setTime(1, Time.valueOf(now));
                out.setString(2, status);
                out.setInt(3, log.getInt("log_id"));
                out.executeUpdate();
            } else {
                status = "Signed In";
                PreparedStatement in = con.prepareStatement("""
                    INSERT INTO attendance_logs (staff_id, log_date, sign_in_time, status, unit)
                    VALUES (?, ?, ?, ?, ?)
                """);
                in.setInt(1, staffId);
                in.setDate(2, Date.valueOf(today));
                in.setTime(3, Time.valueOf(now));
                in.setString(4, status);
                in.setString(5, unit);
                in.executeUpdate();
            }
        }

        // ✅ Show success message
        statusLabel.setText(status + " at " + now);

        // ✅ Add to JTable on top
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.insertRow(0, new Object[]{name, unit, status, now.toString()});

        // ✅ Send email asynchronously
        new Thread(() -> {
            try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/staffattendanceandshiftmanagementschema", "root", "Adesokan2310*")) {
                PreparedStatement ps = conn.prepareStatement("SELECT email FROM users_tb WHERE staff_id = ?");
                ps.setInt(1, staffId);
                ResultSet rs = ps.executeQuery();
                if (rs.next()) {
                    String email = rs.getString("email");
                    String message = """
                        Dear %s,

                        This is to confirm your %s on %s at %s.
                        Unit: %s

                        If you did not perform this action, please report immediately.

                        Regards,
                        PAUSAAMS System
                    """.formatted(name, status, today.toString(), now.toString(), unit);

                    EmailSender.sendSimpleEmail(email, "Attendance Confirmation - " + status, message);
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }).start();

    } catch (Exception e) {
        statusLabel.setText("DB Error: " + e.getMessage());
    }
}



